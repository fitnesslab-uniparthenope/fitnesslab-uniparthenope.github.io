let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                // Use innerHTML to support HTML tags in translations
                element.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Update language dropdown checkmarks
    const checkEn = document.getElementById('check-en');
    const checkIt = document.getElementById('check-it');
    if (checkEn) checkEn.style.display = lang === 'en' ? 'inline' : 'none';
    if (checkIt) checkIt.style.display = lang === 'it' ? 'inline' : 'none';
}

// Initialize language on page load
function initLanguage() {
    setLanguage(currentLanguage);
    
    // Add click handlers for language switcher
    document.querySelectorAll('[data-lang]').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = link.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
}

// Run when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    // DOM is already ready
    initLanguage();
}
